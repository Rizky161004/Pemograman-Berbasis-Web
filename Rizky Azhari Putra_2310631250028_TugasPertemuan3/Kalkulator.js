const tambah = (a, b) => a + b;
const kurang = (a, b) => a - b;
const kali = (a, b) => a * b;
const bagi = (a, b) => a / b;
const modulus = (a, b) => a % b;

console.log("Hasil Perhitungan Kalkulator :");
console.log("8 + 2 =", tambah(8, 2));
console.log("7 - 3 =", kurang(7, 3));
console.log("5 * 2 =", kali(5, 2));
console.log("10 / 2 =", bagi(10, 2));
console.log("10 % 3 =", modulus(10, 3));